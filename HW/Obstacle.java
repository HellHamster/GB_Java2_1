package Java_2_lesson_1.HW;

public class Obstacle {
    private int HEIGHT;
    private int LENGTH;

    public Obstacle (int HEIGHT, int LENGTH) {
        this.HEIGHT = HEIGHT;
        this.LENGTH = LENGTH;
    }

    public int getHEIGHT() {
        return HEIGHT;
    }

    public int getLENGTH() {
        return LENGTH;
    }
}
